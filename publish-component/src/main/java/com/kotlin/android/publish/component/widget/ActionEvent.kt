package com.kotlin.android.publish.component.widget

/**
 * 点击事件类型
 *
 * Created on 2020/7/13.
 *
 * @author o.s
 */
enum class ActionEvent {

    /**
     * 点击关闭
     */
    EVENT_CLOSE,

    /**
     * 点击图片描述
     */
    EVENT_DESC,

    /**
     * 点击生成超链接
     */
    EVENT_LINK,

    /**
     * 长按移动
     */
    EVENT_MOVE
}