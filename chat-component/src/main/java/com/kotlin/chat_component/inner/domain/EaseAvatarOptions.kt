package com.kotlin.chat_component.inner.domain

class EaseAvatarOptions {
    /**
     * 设置item中的头像形状
     * 0：默认，1：圆形，2：矩形
     * @param avatarShape
     */
    var avatarShape = 0

    /**
     * 设置倒角
     *
     * @param avatarRadius
     */
    var avatarRadius = 0

    /**
     * 设置头像控件边框颜色
     *
     * @param avatarBorderColor
     */
    var avatarBorderColor = 0

    /**
     * 设置头像控件边框宽度
     *
     * @param avatarBorderWidth
     */
    var avatarBorderWidth = 0
}